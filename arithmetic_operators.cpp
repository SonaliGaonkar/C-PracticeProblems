#include <iostream>

int main(){
    
    int num_1 = 9;
    int num_2 = 4;

    std::cout << "Addition operation       : " << (num_1 + num_2) << "\n" ;
    std::cout << "Subtraction operation    : " << (num_1 - num_2) << "\n" ;
    std::cout << "Multiplication operation : " << (num_1 * num_2) << "\n" ;
    std::cout << "Division operation       : " << (num_1 / num_2) << "\n" ;
    std::cout << "Modulus operation        : " << (num_1 % num_2) << "\n" ;
    return 0;
}